#include "ft_fdf.h"

void	ft_put_px(t_env *e, int x, int y)
{
	int xt;
	int yt;

	xt = x + e->a;
	yt = y + e->aa;
	if ((xt > 0 && yt > 0) && (xt < 1280 && yt < 720))
	{
		ft_color(e);
		e->b = (e->b > 255) ? 255 : e->b;
		e->r = (e->r > 255) ? 255 : e->r;
		e->g = (e->g > 255) ? 255 : e->g;
		e->r = (e->r < 0) ? 10 : e->r;
		e->b = (e->b < 0) ? 10 : e->b;
		e->g = (e->g < 0) ? 10 : e->g;
		e->data[(xt * 4) + (yt * 1280 * 4)] = e->b;
		e->data[(xt * 4) + (yt * 1280 * 4) + 1] = e->g;
		e->data[(xt * 4) + (yt * 1280 * 4) + 2] = e->r;
	}
}
