#include "ft_fdf.h"

void	ft_count_max(t_env *e)
{
	int j;

	e->hmax = 0;
	j = 0;
	e->smax = 0;
	while (e->map[e->hmax] != 0)
	{
		while (e->map[e->hmax][j].k != 0)
		{
			j++;
		}
		if (j > e->smax)
			e->smax = j;
		j = 0;
		e->hmax++;
	}
}
