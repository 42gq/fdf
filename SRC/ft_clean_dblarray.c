#include "ft_fdf.h"

void	ft_clean_dblarray(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
		i++;
	while (i >= 0)
	{
		free(tab[i]);
		tab[i] = NULL;
		i--;
	}
}
